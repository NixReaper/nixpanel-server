Admin User        : ${ADMIN_USER}
Admin Password    : ${ADMIN_PASS}
Admin Email       : ${ADMIN_EMAIL}
MariaDB Root Pass : ${MARIADB_ROOT_PASS}
Install Dir       : ${INSTALL_DIR}
Install Log       : ${LOG_FILE}
CREDS
chmod 600 "$CRED_FILE"

